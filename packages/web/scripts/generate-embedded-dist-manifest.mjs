#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const webRoot = path.resolve(__dirname, '..');
const distRoot = path.join(webRoot, 'dist');
const outputPath = path.join(webRoot, 'server', 'lib', 'opencode', 'embedded-dist-manifest.generated.js');

function walkFiles(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
      continue;
    }
    if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files;
}

if (!fs.existsSync(distRoot)) {
  throw new Error(`Missing dist directory: ${distRoot}`);
}

const files = walkFiles(distRoot)
  .map((filePath) => path.relative(distRoot, filePath).replaceAll(path.sep, '/'))
  .sort();

const imports = files.map((file, index) => {
  const specifier = `../../../dist/${file}`;
  return `import file_${index} from ${JSON.stringify(specifier)} with { type: "file" };`;
});

const entries = files.map((file, index) => `  ${JSON.stringify(`/${file}`)}: file_${index},`);

const content = `// Generated by packages/web/scripts/generate-embedded-dist-manifest.mjs.
// Do not edit by hand.

${imports.join('\n')}

export const embeddedDistFiles = Object.freeze({
${entries.join('\n')}
});
`;

fs.writeFileSync(outputPath, content);
console.log(`Generated ${outputPath} with ${files.length} embedded dist files.`);
